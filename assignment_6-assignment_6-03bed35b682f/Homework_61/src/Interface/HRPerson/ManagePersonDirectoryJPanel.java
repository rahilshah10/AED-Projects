    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblPerson.getSelectedRow();
        if(selectedRow >= 0)
        {
        int dialogButton = JOptionPane.YES_NO_OPTION;
        int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to delete this Person?", "Warning", dialogButton);
        if(dialogResult == JOptionPane.YES_OPTION)
        {
            Person p = (Person)tblPerson.getValueAt(selectedRow, 0);
            business.getPersonDirectory().removePerson(p);
            business.getUserDirectory().deleteAll(p);
            populateTable();
        }
        }
        
        else
        {
        JOptionPane.showConfirmDialog(null,"Please Select a Row","Warning", JOptionPane.WARNING_MESSAGE);
        }
    
            
    }//GEN-LAST:event_btnDeleteActionPerformed


