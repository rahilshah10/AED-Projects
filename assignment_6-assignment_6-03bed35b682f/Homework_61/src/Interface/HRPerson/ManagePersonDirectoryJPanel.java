    public void populateTable()
    {
       DefaultTableModel dtm=(DefaultTableModel) tblPerson.getModel();
        dtm.setRowCount(0);
        int rowCount = dtm.getRowCount();
    for (int i = rowCount - 1; i >= 0; i--) {
         dtm.removeRow(i);
        }
        
        for(UserAccount userAccount: business.getUserDirectory().getUserAccountDir())
        {
            Object[] row=new Object[3];
            row[0]=userAccount.getUperson();
            row[1]=userAccount.getUperson().getDateOfBirth();
            row[2]=userAccount.getUserName();
            dtm.addRow(row);
        }
        
    }
