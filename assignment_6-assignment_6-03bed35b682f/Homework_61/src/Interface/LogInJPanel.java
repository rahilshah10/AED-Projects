    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        if(txtUserName.getText().isEmpty() )
        {
            JOptionPane.showMessageDialog(null, "Please give username");
        }
        else if(String.valueOf(txtPasswordField.getPassword()).equalsIgnoreCase(""))
        {
            JOptionPane.showMessageDialog(null, "Please give password");
        }
        else
        {
        String username=txtUserName.getText();
        String paswd=String.valueOf(txtPasswordField.getPassword());

        UserAccountDirectory ud= business.getUserDirectory();
        UserAccount u=ud.searchAccount(username);
        
        if(u==null)
        {   
            JOptionPane.showMessageDialog(null, "The user name or password you entered isn't correct. Try entering it again.");
            txtPasswordField.setText("");
        }
            else
            {
                if(u.getPassWord()==paswd.hashCode() && u.isAccountStatus())
                     {
                        if(u.getRole().equalsIgnoreCase("SystemAdmin"))
                            {
                                 SystemAdminWorkAreaJPanel panel=new SystemAdminWorkAreaJPanel(userProcessContainer,business,u);
                                 userProcessContainer.add("SystemAdminWorkAreaJPanel",panel);
                                 CardLayout layout=(CardLayout) userProcessContainer.getLayout();
                                 layout.next(userProcessContainer);
    
                            }
                        else if (u.getRole().equalsIgnoreCase("HRManager"))
                        {
                                DisplayHRInfo panel=new DisplayHRInfo(userProcessContainer,business,u);
                                 userProcessContainer.add("DisplayHRInfo",panel);
                                 CardLayout layout=(CardLayout) userProcessContainer.getLayout();
                                 layout.next(userProcessContainer);
                        }
                        else if(u.getRole().equalsIgnoreCase("Salesman"))
                        {
                            ManageCustomerJPanel panel=new ManageCustomerJPanel(userProcessContainer,business,u);
                            userProcessContainer.add("DisplaySalesman",panel);
                            CardLayout layout=(CardLayout) userProcessContainer.getLayout();
                            layout.next(userProcessContainer);
                        }
                        else if(u.getRole().equalsIgnoreCase("Manager"))
                        {
                            LoginSupplier panel = new LoginSupplier(userProcessContainer,business,u);
                            userProcessContainer.add("SupplierManager",panel);
                            CardLayout layout=(CardLayout) userProcessContainer.getLayout();
                            layout.next(userProcessContainer);
                        }
               
                        txtUserName.setText("");
                        txtPasswordField.setText("");
            }
                else 
                {
                    JOptionPane.showMessageDialog(null, "The user name or password you entered isn't correct. Try entering it again.");
                    txtPasswordField.setText("");
                }
       
        }
        }
    }//GEN-LAST:event_btnLoginActionPerformed


