    private void btnServeCustomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnServeCustomerActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(txtCustSearch.getText());
        ArrayList<Market> ml = business.getMarketList().getMarketList();
        for(Market m : ml)
        {
            Customer c = m.searchCustomerID(id);
            if(c!=null)
            {
                ServeCustomerJPanel panel = new ServeCustomerJPanel(business,userProcessContainer,id, userAccount,m,c);
                userProcessContainer.add("serveCustomer",panel);
                CardLayout layout=(CardLayout) userProcessContainer.getLayout();
                layout.next(userProcessContainer);
            }
        }
    }//GEN-LAST:event_btnServeCustomerActionPerformed

