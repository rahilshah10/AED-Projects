    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        try{
           
            Pattern pattern = Pattern.compile("[a-zA-Z_0-9]");
            Matcher m= pattern.matcher(txtAccountNumber.getText() + txtBankName.getText() + txtRoutingNumber.getText());
            boolean boo = m.find();
            if(boo == false){
            int balance = Integer.parseInt(txtBalance.getText());
            JOptionPane.showMessageDialog(null, "Please remove special character from text field");
            }
            else{
        String routingNumber = txtRoutingNumber.getText();
        String accountNumber = txtAccountNumber.getText();
        String bankName = txtBankName.getText();
        int balance = Integer.parseInt(txtBalance.getText());
        
        Account account = accountDirectory.addAccount();
        account.setRoutingNo(routingNumber);
        account.setAccountNo(accountNumber);
        account.setBankName(bankName);
        account.setBal(balance);
            
        JOptionPane.showMessageDialog(null,"Account Successfully Created");
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, "Please enter correct data");
        }
    }//GEN-LAST:event_btnCreateActionPerformed

