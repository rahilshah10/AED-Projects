    private void btnViewFlightActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewFlightActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable.getSelectedRow();
        if(selectedRow < 0)
        {
       JOptionPane.showConfirmDialog(null,"Please Select a Row to view details","Warning", JOptionPane.WARNING_MESSAGE);
        
        }
        else
        {
            Flight fly = (Flight) jTable.getValueAt(selectedRow, 0);
            ViewFlightDetailsJPanel panel = new ViewFlightDetailsJPanel(userProcessContainer,fly,travelagent);
            userProcessContainer.add("ViewFlightDetailsJPanel",panel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);       
        }
    }//GEN-LAST:event_btnViewFlightActionPerformed

