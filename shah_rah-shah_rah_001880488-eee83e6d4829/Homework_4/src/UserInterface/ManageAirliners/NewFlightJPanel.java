    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        try{
        String FlightNumber = txtFlightNumber.getText();
        String Date = txtDate.getText();
        String Airport = txtAirport.getText();
        String Destination = txtDestination.getText();
        String Preferredtime = String.valueOf(comboPreferred.getSelectedItem());
        String SerialNo = txtSerialNo.getText();
        String Price = txtPrice.getText();
        String Time = txtTime.getText();
        String AvailS = txtAvailSeats.getText();
        
        Pattern p1 = Pattern.compile("^[a-zA-Z0-9]+$");
        Pattern p2 = Pattern.compile("^(0?[1-9]|[12][0-9]|3[01])[\\/\\-](0?[1-9]|1[012])[\\/\\-]\\d{4}$");
        Pattern p3 = Pattern.compile("^[0-9]+$");
        Matcher m1 = p1.matcher(FlightNumber);
        Matcher m2 = p2.matcher(Date);
        Matcher m3 = p1.matcher(Airport);
        Matcher m4 = p1.matcher(Destination);
        Matcher m5 = p3.matcher(SerialNo);
        Matcher m6 = p3.matcher(Price);
        Matcher m7 = p1.matcher(Time);
        Matcher m8 = p3.matcher(AvailS);
        
        if(!m1.matches() || !m2.matches() || !m3.matches() || !m4.matches() || !m5.matches() || !m6.matches() || !m7.matches() || !m8.matches())
                {
                JOptionPane.showMessageDialog(null, "Please enter a valid input", "Error", JOptionPane.ERROR_MESSAGE);
                txtFlightNumber.setText("");
                txtDate.setText("");
                txtAirport.setText("");
                txtDestination.setText("");
                txtSerialNo.setText("");
                txtPrice.setText("");
                txtTime.setText("");
                txtAvailSeats.setText("");
                }
        else{
                       

        Flight f1 = airli.getFlight().addData();
        f1.setModelNo(FlightNumber);
        f1.setDate(Date);
        f1.setAirport(Airport);
        f1.setDestination(Destination);
        f1.setPreferredtime(Preferredtime);
        f1.setSerialNo(SerialNo);
        f1.setPrice(Price);
        f1.setTime(Time);
        f1.setAvailSeats(AvailS);

        JOptionPane.showMessageDialog(null,"Account Successfully Created");
    }//GEN-LAST:event_btnCreateActionPerformed
        }  catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, "Please enter correct data"); 
        }}
