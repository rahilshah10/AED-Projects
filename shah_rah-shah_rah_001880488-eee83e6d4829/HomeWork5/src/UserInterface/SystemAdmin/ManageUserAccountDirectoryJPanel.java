    public void populateTable()
    {
       DefaultTableModel dtm=(DefaultTableModel) tblSystemAdmin.getModel();
        dtm.setRowCount(0);
        int rowCount = dtm.getRowCount();
    for (int i = rowCount - 1; i >= 0; i--) {
         dtm.removeRow(i);
        }
        
        for(UserAccount u: business.getuDir().getUserAccountDir())
        {
            if(u.getUserName()=="" && u.getPassWord()=="")
            {
            continue;
            }
            else
            {
            Object[] row=new Object[3];
            row[0]=u;
            row[1]=u.getPassWord();
            row[2]=u.getRole();
            dtm.addRow(row);
            }
        }
        
    }
