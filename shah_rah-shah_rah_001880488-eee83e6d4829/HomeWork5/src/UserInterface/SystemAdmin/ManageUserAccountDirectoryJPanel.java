    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblSystemAdmin.getSelectedRow();
        if(selectedRow >= 0)
        {
        int dialogButton = JOptionPane.YES_NO_OPTION;
        int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to delete this Person?", "Warning", dialogButton);
        if(dialogResult == JOptionPane.YES_OPTION)
        {
            UserAccount u = (UserAccount)tblSystemAdmin.getValueAt(selectedRow, 0);
            business.getuDir().deleteAccount(u);
            populateTable();
        }
        }
        
        else
        {
        JOptionPane.showConfirmDialog(null,"Please Select a Row","Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed


