    public void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblStorageStaff.getModel();
        dtm.setRowCount(0);

        for (Product sp : system.getProductdirectory().getProductlist()) {

            Object[] row = new Object[5];
            row[0] = sp.getProducerName();
            row[1] = sp;
            row[2] = sp.getQuantity();
            row[3] = sp.getTemperature();
            row[4] = sp.getHumidity();
            dtm.addRow(row);

        }

    }

    public void populateAdvice() {
        if (organization.getWorkQueue().getWorkRequestList().size() != 0) 
        {
            int a = organization.getWorkQueue().getWorkRequestList().size();
            WorkRequest req = organization.getWorkQueue().getWorkRequestList().get(a - 1);
            lbAdvice2.setText(String.valueOf(req.getMessage()));
        }
    }

