    private void txtSetParameterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtSetParameterActionPerformed
        // TODO add your handling code here:
         try {

            Pattern p1 = Pattern.compile("^[0-9]+$");
            Matcher m1 = p1.matcher(txtHumidity.getText());
            Matcher m2 = p1.matcher(txtTempreture.getText());
              if (!m1.matches() || !m2.matches()) {
                JOptionPane.showMessageDialog(null, "Please enter a valid input", "Error", JOptionPane.ERROR_MESSAGE);
                txtHumidity.setText("");
                txtTempreture.setText("");
                } else {
                   int selectedRow = tblStorageStaff.getSelectedRow();
                
                if (selectedRow < 0) {
                    return;
                }
                
                Product product = (Product) tblStorageStaff.getValueAt(selectedRow, 1);
            
            product.setHumidity(Integer.parseInt(txtHumidity.getText()));
            product.setTemperature(Integer.parseInt(txtTempreture.getText()));
            populateTable();
              JOptionPane.showMessageDialog(null, "Storage Data Successfully Created");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Please enter correct data");
        }
    }//GEN-LAST:event_txtSetParameterActionPerformed

